# Sohel Tamboli

# 1. Creating a tuple
my_tuple = (10, 20, 30, 40, 50)
print("Original Tuple:", my_tuple)

# 2. Accessing elements
print("First element:", my_tuple[0])
print("Last element:", my_tuple[-1])

# 3. Tuple concatenation

tuple2 = (60, 70, 80)
combined_tuple = my_tuple + tuple2
print("Concatenated Tuple:", combined_tuple)

# 4. Slicing the tuple

print("Slice my_tuple[1:4]:", my_tuple[1:4])


# 5. Length of the tuple
print("Length of original tuple:", len(my_tuple))
print("Length of concatenated tuple:", len(combined_tuple))
