# Sohel Tamboli

a = int(input("Enter first number : "))
b = int(input("Enter second number: "))

# Performing arithmetic operations

c = a + b        # Addition
d = a - b        # Subtraction
e = a * b        # Multiplication
f = a / b        # Division
g = a % b        # Modulus

# Displaying results
print("\nResults:")
print("Addition (c):", c)
print("Subtraction (d):", d)
print("Multiplication (e):", e)
print("Division (f):", f)
print("Modulus (g):", g)
